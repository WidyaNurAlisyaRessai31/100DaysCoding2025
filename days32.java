public class days32 {
    public static void main(String[] args) {
        int x = 10;
        int y = 5;

        // Operator logika OR (||)
        // Kondisi akan bernilai true jika salah satu bernilai true
        if (x > 8 || y < 3) {
            System.out.println("Salah satu kondisi benar.");
        } else {
            System.out.println("Kedua kondisi salah.");
        }
    }
}
