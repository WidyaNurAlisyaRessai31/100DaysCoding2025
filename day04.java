public class day04 {
    public static void main(String[] args) {
        // [\n] membuat baris baru setelah "Halo,"
        System.out.println("Halo,\nSelamat datang di Java!");

        // [\t] membuat indentasi seperti tab pada teks
        System.out.println("Nama\t: Wiwi Ressai");
        System.out.println("Usia\t: 21");
        System.out.println("Kota\t: Mamuju");

        // [\r] menimpa "Malam" dengan "Siang"
        System.out.println("Selamat Malam\rSelamat Siang");

        // [\"] mengizinkan penggunaan tanda kutip ganda di dalam string
        System.out.println("Dia berkata, \"Halo, apa kabar?\"");

        // [\\] digunakan untuk mencetak garis miring terbalik
        System.out.println("Jalur file: C:\\Users\\Public\\Document");

    }
}
