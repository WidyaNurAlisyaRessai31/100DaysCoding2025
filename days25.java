public class days25 {
    public static void main(String[] args) {
        int angka = 5; // nilai awal

        System.out.println("Nilai awal: " + angka);

        angka++; // naik 1
        System.out.println("Setelah angka++: " + angka);

        angka--; // turun 1
        System.out.println("Setelah angka--: " + angka);
    }
}
