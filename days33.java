public class days33 {
    public static void main(String[] args) {
        int angka = 10;

        // Mengecek apakah angka bukan nol menggunakan NOT
        boolean bukanNol = !(angka == 0);

        System.out.println("Angka: " + angka);
        System.out.println("Apakah angka bukan nol? " + bukanNol);
    }
}
